function CartesianValues4(x, y, z, w)
{
	this.x = x;
	this.y = y;
	this.z = z;
	this.w = w;
}

CartesianValues4.getX = function()
{
	return this.x;
};

CartesianValues4.getY = function()
{
	return this.y;
};

CartesianValues4.getZ = function()
{
	return this.z;
};

CartesianValues4.getW = function()
{
	return this.w;
};
